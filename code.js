function sayHello(input) {
    if (input === undefined){
        return ""
    } if (input === "Alex") {
        return "Hello, Alex!";
    } if (input === "Pat") {
        return "Hello, Pat!";
    } if (input === "Jane"){
        return "Hello, Jane!";
    }
}
